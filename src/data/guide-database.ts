export const getTopicById = (id: string): GuideTopic | undefined => {
  return guideTopics.find((t) => t.id === id);
};

export const getTopicBySlug = (slug: string): GuideTopic | undefined => {
  return guideTopics.find((t) => t.slug === slug);
};

export const getTopicsBySection = (sectionId: string): GuideTopic[] => {
  // Map section IDs to topic categories based on tags/content
  const sectionMapping: Record<string, string[]> = {
    login: ['login-main', 'login-remote', 'login-2fa', 'login-password'],
    startpage: ['startpage-main', 'profile', 'elearning', 'reminders'],
    calendar: ['calendar-main', 'calendar-ical'],
    requests: guideTopics.filter((t) => t.id.startsWith('requests-')).map((t) => t.id),
    time: ['time-log', 'time-registration', 'time-registration-new', 'time-flextime', 'time-yearwork'],
    absence: ['absence-main', 'absence-vacation'],
    banks: ['banks-main'],
    salary: ['salary-payslips', 'salary-overview', 'salary-supplements', 'salary-distributions'],
    shifts: guideTopics.filter((t) => t.id.startsWith('shifts-')).map((t) => t.id),
    mobile: ['mobile-apps', 'mobile-gatgo-setup'],
    leader: ['leader-dashboard', 'leader-absence', 'leader-shiftbook'],
    system: guideTopics.filter((t) => t.id.startsWith('system-') || t.id.startsWith('dept-')).map((t) => t.id),
  };

  const topicIds = sectionMapping[sectionId] || [];
  return topicIds.map((id) => getTopicById(id)).filter((t): t is GuideTopic => t !== undefined);
};

export const getChildTopics = (parentId: string): GuideTopic[] => {
  return guideTopics.filter((t) => t.parentId === parentId);
};

export const getRootTopics = (): GuideTopic[] => {
  return guideTopics.filter((t) => t.parentId === null);
};

export const searchTopics = (query: string): GuideTopic[] => {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return [];

  return guideTopics.filter((topic) => {
    const searchableText = [
      topic.title,
      topic.titleNo,
      topic.description,
      topic.descriptionNo,
      ...topic.tags,
    ]
      .join(' ')
      .toLowerCase();

    return searchableText.includes(normalizedQuery);
  });
};
